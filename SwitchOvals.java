public class SwitchOvals {
    public static void main(String[] args)
    {
        char ch='i';
         switch(ch)
         {
            case 'a':
              System.out.println("a is the oval");
              break;
            case 'e':
            System.out.println("e is the oval");
            break;
            case 'i':
            System.out.println( "i is the oval");
            break;
            case 'o':
            System.out.println( "o is the oval");
            break;
         }
    }
}
